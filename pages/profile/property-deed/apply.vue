<template>
  <div>
    <div class="d-flex justify-content-between align-items-center">
      <h5>Property deed apply</h5>
    </div>
    <div class="card-body p-0 mt-4">
      <div class="search d-flex justify-content-between align-items-center">
        <div class="form-group">
          <input class="form-control custom-form-control" type="text" v-model="tableData.search"
            placeholder="Search Table" @input="getData()">
        </div>
        <div class="form-group">
          <select class="form-control custom-select-form-control" v-model="tableData.length" @change="getData()">
            <option v-for="(records, index) in perPage" :key="index" :value="records">{{ records }}</option>
          </select>
        </div>
      </div>
      <DataTable id="dataTable" :columns="columns" :sortKey="sortKey" :sortOrders="sortOrders" @sort="sortBy" class="">
        <tbody>
          <tr v-for="(value, i) in values" :key="value.id">
            <td>{{ i + 1 }}</td>
            <td>
              {{ value.landlord.name }}
            </td>
            <td>
              <nuxt-link :to="{ name: 'profile-property-id-details', params: { id: value.property_id } }"
                title="Details">
                {{ value.property.name }}
              </nuxt-link>
            </td>
            <td>
              {{ value.start_date ?? 'Not start' }}
            </td>
            <td>
              <b-badge v-if="value.status === 0" variant="warning">Pending</b-badge>
              <b-badge v-if="value.status === 1" variant="secondary">Viewed</b-badge>
              <b-badge v-if="value.status === 2" variant="success">Accepted</b-badge>
              <b-badge v-if="value.status === 3" variant="danger">Declined</b-badge>
              <b-badge v-if="value.status === 4" variant="primary">Information Submited</b-badge>
            </td>
            <td>
              <nuxt-link v-if="value.status === 2" :to="{ name: 'profile-property-deed-id-information', params: { id: value.id } }" rel="tooltip"
                class="btn btn-sm btn-success btn-simple" title="submit your information">
                Submit Info
              </nuxt-link>

              <!-- <nuxt-link :to="{ name: 'profile-property-deed-id-get-rent', params: { id: value.id } }"
                rel="tooltip" class="btn btn-sm btn-secondary btn-simple" title="Get payment">
                <font-awesome-icon icon="fa-solid fa-hand-holding-dollar" />
              </nuxt-link> -->
            </td>
          </tr>
        </tbody>
      </DataTable>

      <pagination :pagination="pagination" @prev="getData(pagination.prevPageUrl)"
        @next="getData(pagination.nextPageUrl)">
      </pagination>
    </div>
  </div>
</template>

<script>

import Pagination from "@/components/Datatable/Pagination";
import DataTable from "@/components/Datatable/DataTable";

export default {
  layout: 'dashboard',
  name: "deed-apply",
  components: { DataTable, Pagination },
  created() {
    this.getData();
  },
  data() {
    let sortOrders = {};
    let columns = [
      { width: '', label: 'Sl', name: 'id' },
      { width: '', label: 'Landlord', name: 'name' },
      { width: '', label: 'Property', name: 'property' },
      { width: '', label: 'Start date', name: 'start_date' },
      { width: '', label: 'Status', name: 'status' },
      { width: '', label: 'Actions', name: 'actions' },
    ];
    columns.forEach((column) => {
      sortOrders[column.name] = -1;
    });
    return {
      values: [],
      sum: [],
      columns: columns,
      sortKey: 'id',
      sortOrders: sortOrders,
      perPage: ['10', '25', '50', '100', '500', '2000', 'all'],
      tableData: {
        draw: 0,
        length: 10,
        search: '',
        column: 0,
        dir: 'desc',
        userId: this.$auth.user.id
      },
      pagination: {
        lastPage: '',
        currentPage: '',
        total: '',
        lastPageUrl: '',
        nextPageUrl: '',
        prevPageUrl: '',
        from: '',
        to: '',
      },
    }
  },
  methods: {
    getData(url = 'property/deed/apply-list') {
      this.tableData.draw++;
      this.$axios.post(url, { params: this.tableData })
        .then(response => {
          let data = response.data;
          if (this.tableData.draw == data.draw) {
            this.values = data.data.data;
            this.configPagination(data.data);
          }
        })
        .catch(errors => {
          alert(errors);
        })
    },

    configPagination(data) {
      this.pagination.lastPage = data.last_page;
      this.pagination.currentPage = data.current_page;
      this.pagination.total = data.total;
      this.pagination.lastPageUrl = data.last_page_url;
      this.pagination.nextPageUrl = data.next_page_url;
      this.pagination.prevPageUrl = data.prev_page_url;
      this.pagination.from = data.from;
      this.pagination.to = data.to;
    },

    sortBy(key) {
      this.sortKey = key;
      this.sortOrders[key] = this.sortOrders[key] * -1;
      this.tableData.column = this.getIndex(this.columns, 'name', key);
      this.tableData.dir = this.sortOrders[key] === 1 ? 'asc' : 'desc';
      this.getData();
    },
    getIndex(array, key, value) {
      return array.findIndex(i => i[key] == value)
    },
  }
}
</script>

